package pojo;

public class SpriteInfo {
    public String imageFileId;
    public String x;
    public String y;
    public String width;
    public String height;

    public SpriteInfo(String imageFileId, String x, String y, String width, String height) {
        this.imageFileId = imageFileId;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}

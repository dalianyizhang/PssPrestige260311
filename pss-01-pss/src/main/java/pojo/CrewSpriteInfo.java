package pojo;

public class CrewSpriteInfo {
    public String headSpriteId;
    public String bodySpriteId;
    public String legSpriteId;

    public CrewSpriteInfo(String headSpriteId, String bodySpriteId, String legSpriteId) {
        this.headSpriteId = headSpriteId;
        this.bodySpriteId = bodySpriteId;
        this.legSpriteId = legSpriteId;
    }
}